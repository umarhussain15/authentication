  <h1 style="color:red;">How to Prepare to Donate Blood</h1>
  <p>The availability of quality blood is a very important component of modern medicine.
  It cannot be created synthetically, so it must be collected from volunteer donors. However,
  many people are afraid to donate for reasons varying from worry about pain to catching a disease.
  Donating blood is safe due to the many precautions in place, which means there is no reason to fear giving blood.
  The most serious risks when donating blood are small adverse reactions such as dizziness, fainting, or bruising.
  If you follow a few simple steps, you will be as prepared as possible to give blood.</p>
  <p style="color:red;font-size:15px;">Getting Ready to Donate</p>
  <ol>
	<li><p style="font-size:14px;">Determine if you are eligible.</p> 
	Each country's blood service has different requirements in place to be eligible to donate blood.
	These can range from blood disease concerns, to past travel locations, to age and weight.
	In general, you will be able to give blood if you meet certain criteria.
	You have to be healthy, fit, and not suffering from a current illness. Avoid donating blood if you have a cold, a cold sore, a cough, a virus,
	or an upset stomach.
	You must weigh at least 110 pounds or 50 kg. You have to be old enough. In many jurisdictions, parental permission is required for the ages
	16-17 to give blood. Check the blood organization in your area if you are around this age.
	You can only donate blood every 56 days. If you have donated blood more recently than that, then you are not eligible again. Do not give blood if you
	have had simple dental work done within 24 hours or major dental work in the past month.</li>
	
	<li><p style="font-size:14px;">Eat iron rich foods</p> 
	Since blood production requires iron, you should eat iron rich foods for two weeks before your appointment.
	This will help you have strong blood for donation and help you better recuperate after your donation.
	Iron rich foods include spinach, whole grains, fish, poultry, beans, eggs, and beef. Having good levels of vitamin C will
	also help to increase iron absorption. Try to consume citrus fruit, juices, or vitamin C supplements.</li>
	
	<li><p style="font-size:14px;">Hydrate yourself</p> 
	To prepare your body for the loss of blood, you need to drink plenty of water or fruit juice the night and morning before you donate.
	The leading cause of faintness and dizziness when you give blood is a drop in blood pressure or blood sugar. The risk of this is greatly reduced
	if you are more hydrated when you visit the donation center. It is recommended you drink a lot in the 24 hours leading to the donation time,
	especially when it's warm. This includes drinking 4 good sized glasses of water or juice during the 3 hours leading up to your donation. If you
	are donating plasma or platelets, drink at least 6 to 8 glasses of fluids.</li>
	
	<li><p style="font-size:14px;">Get a good night's rest</p> 
	Before you donate blood, you should have proper sleep. This will help you feel better and more alert when you give blood, which will help
	reduce the risk of any adverse reactions to the process. This means you should get at least 5-7 hours of sleep before you donate blood.</li>
	
	<li><p style="font-size:14px;">Avoid certain activities</p> 
	In the hours leading up to your appointment, you need to avoid certain activities that may hurt your chances of donating or contaminate your blood.
	You should not smoke within the hour leading up to your appointment. You should also avoid alcoholic beverages in the 24 hours before you donate.
	You should also not chew gum, mints, or candy in the hours before your donation. Chewing gum, mints, or candy makes the temperature in your mouth
	go up, which could make it seem like you have a fever and make you ineligible to give blood. If you are giving platelets, you should avoid taking
	aspirin or other NSAIDs for two days before you donate.</li>
  </ol>
